-- CREATE TABLE
CREATE TABLE PERSON
(
    IDCARD_NO             VARCHAR2(18) NOT NULL,
    NAME                  VARCHAR2(40) NOT NULL,
    GENDER                VARCHAR2(10),
    BIRTH_DATE            DATE,
    AGE                   NUMBER,
    NATIVE_PLACE_CODE     NUMBER,
    PROFESSION_CODE       NUMBER,
    GRANDUATE_SCHOOL_CODE NUMBER,
    CREATED_DATE          DATE,
    CREATED_BY            VARCHAR2(40),
    UPATE_DATE            DATE,
    UPDATE_BY             VARCHAR2(40),
    HOME_PROVINCE         NUMBER(6),
    HOME_CITY             NUMBER(6),
    HOME_AREA             NUMBER(6),
    WORK_PROVINCE         NUMBER(6),
    WORK_CITY             NUMBER(6),
    WORK_AREA             NUMBER(6)
)
    TABLESPACE USERS
  PCTFREE 10
  INITRANS 1
  MAXTRANS 255
  STORAGE
  (
    INITIAL 64K
    NEXT 1M
    MINEXTENTS 1
    MAXEXTENTS UNLIMITED
  );
-- ADD COMMENTS TO THE COLUMNS
COMMENT ON COLUMN PERSON.IDCARD_NO
  IS '身份证号';
