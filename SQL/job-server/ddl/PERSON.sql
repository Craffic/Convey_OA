  IS '更新人';
COMMENT ON COLUMN PERSON.HOME_PROVINCE
  IS '家庭地址（省）';
COMMENT ON COLUMN PERSON.HOME_CITY
  IS '家庭地址（市）';
COMMENT ON COLUMN PERSON.HOME_AREA
  IS '家庭地址（区）';
COMMENT ON COLUMN PERSON.WORK_PROVINCE
  IS '工作地址（省）';
