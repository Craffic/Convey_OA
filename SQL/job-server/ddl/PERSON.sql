COMMENT ON COLUMN PERSON.GRANDUATE_SCHOOL_CODE
  IS '毕业学校编码';
COMMENT ON COLUMN PERSON.CREATED_DATE
  IS '创建时间';
COMMENT ON COLUMN PERSON.CREATED_BY
  IS '创建人';
COMMENT ON COLUMN PERSON.UPATE_DATE
  IS '更新时间';
